function main() {
    const PI = Math.PI;
    //Read input using 'readLine()'//
    let r = readLine();
    let area = PI * r ** 2;
    let perimeter = PI * r * 2;
    // Print the area of the circle:
    console.log(area);
    // Print the perimeter of the circle:
    console.log(perimeter);
}